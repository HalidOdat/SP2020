/*
 * Write a program that will print the remainder from the division of number 19 with 2, 3, 5 and 8.
 * */

#include <stdio.h>

int main () {
    printf("The remainder for the division of 19 with 2 is %d\n", 19%2);
    printf("The remainder for the division of 19 with 3 is %d\n", 19%3);
    printf("The remainder for the division of 19 with 5 is %d\n", 19%5);
    printf("The remainder for the division of 19 with 8 is %d\n", 19%8);
    return 0;
}